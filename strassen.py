import numpy as np
a=np.array([[1,2],[3,4]])
b=np.array([[5,6],[7,8]])
print(a)
print(b)
p=(a[0,0]+a[1,1])*(b[0,0]+b[1,1])
q=(a[1,0]+a[1,1])*b[0,0]
r=a[0,0]*(b[0,1]-b[1,1])
s=a[1,1]*(b[1,0]-b[0,0])
t=(a[0,0]+a[0,1])*b[1,1]
u=(a[1,0]-a[0,0])*(b[0,0]+b[0,1])
v=(a[0,1]-a[1,1])*(b[1,0]+b[1,1])
c00=p+s-t+v
c01=r+t
c10=q+s
c11=p+r-q+u
c=np.array([[c00,c01],[c10,c11]])
print("matrix:")
print(c)
